n = int(input())

""" n = 1, 1 => 1
n = 2, 00 11 => 2
n = 3, 100, 111, 001 => 3
n = 4, 1111, 0000, 1001, 0011, 1100 => 5
n = 5, 11111, 11001, 10011, 11100, 00111, 00001, 00100, 10000 => 8
..
n = n-1 + n+2   --- 피보나치수열! dp로 구현 """

dp = [0]*1000001

dp[1] = 1
dp[2] = 2
for i in range(3, n+1):
    dp[i] = (dp[i-1]+dp[i-2])%15746

print(dp[n])    